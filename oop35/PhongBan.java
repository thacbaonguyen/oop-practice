package oop35;

public class PhongBan {
    private String ma;
    private String tenPhongBan;

    public PhongBan(String ma, String tenPhongBan) {
        this.ma = ma;
        this.tenPhongBan = tenPhongBan;
    }

    public String getma() {
        return ma;
    }

    public String gettenPhongBan() {
        return tenPhongBan;
    }
}

