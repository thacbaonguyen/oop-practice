package oop56;

public class Nhom {
	private String maSinhVien;
	private int nhomBaiTap;
	public Nhom(String maSinhVien, int nhomBaiTap) {
		super();
		this.maSinhVien = maSinhVien;
		this.nhomBaiTap = nhomBaiTap;
	}
	public String getMaSinhVien() {
		return maSinhVien;
	}
	public int getNhomBaiTap() {
		return nhomBaiTap;
	}
	
}
