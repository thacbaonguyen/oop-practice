package OOP27;

public class MonHoc {
	private String maMon, tenMon;
	private int soTinChi;
	public MonHoc(String maMon, String tenMon, int soTinChi) {
		super();
		this.maMon = maMon;
		this.tenMon = tenMon;
		this.soTinChi = soTinChi;
	}
	public String getMaMon() {
		return maMon;
	}
	public String getTenMon() {
		return tenMon;
	}
	
}
